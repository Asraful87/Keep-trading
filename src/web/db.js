// Template storage for the Embed Builder dashboard.
//
// Backend selection (in priority order):
//   1. Postgres  — when DATABASE_URL is set (Heroku). Persists across restarts.
//   2. SQLite    — node:sqlite (Node >= 22.5), local file. No native deps.
//   3. JSON      — fallback file store so the dashboard still works.

import { DatabaseSync } from "node:sqlite";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DATA_DIR = path.join(__dirname, "..", "data");
const SQLITE_PATH = path.join(DATA_DIR, "templates.db");
const JSON_PATH = path.join(DATA_DIR, "templates.json");

fs.mkdirSync(DATA_DIR, { recursive: true });

function now() {
  return Math.floor(Date.now() / 1000);
}

const TABLE_SQL = `
  CREATE TABLE IF NOT EXISTS templates (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    guild_id TEXT NOT NULL,
    data TEXT NOT NULL,
    created_at INTEGER NOT NULL,
    updated_at INTEGER NOT NULL,
    created_by TEXT
  );
`;

// ---------------------------------------------------------------------------
// Postgres backend
// ---------------------------------------------------------------------------
async function createPostgresBackend() {
  const pg = (await import("pg")).default;
  const { Pool } = pg;
  const pool = new Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: process.env.DATABASE_URL?.includes("sslmode")
      ? undefined
      : { rejectUnauthorized: false },
  });
  await pool.query(TABLE_SQL.replace(/AUTOINCREMENT/g, "GENERATED BY DEFAULT AS IDENTITY"));

  return {
    kind: "postgres",
    async list(guildId) {
      const { rows } = await pool.query(
        "SELECT id, name, guild_id, created_at, updated_at, created_by FROM templates WHERE guild_id = $1 ORDER BY updated_at DESC",
        [guildId]
      );
      return rows.map((r) => ({
        id: r.id,
        name: r.name,
        guildId: r.guild_id,
        createdAt: r.created_at,
        updatedAt: r.updated_at,
        createdBy: r.created_by,
      }));
    },
    async get(id) {
      const { rows } = await pool.query("SELECT * FROM templates WHERE id = $1", [Number(id)]);
      const r = rows[0];
      if (!r) return null;
      let data = r.data;
      try {
        data = JSON.parse(r.data);
      } catch {
        /* keep as-is if not JSON */
      }
      return { ...r, guildId: r.guild_id, createdBy: r.created_by, data };
    },
    async create({ name, guildId, data, createdBy }) {
      const ts = now();
      const { rows } = await pool.query(
        "INSERT INTO templates (name, guild_id, data, created_at, updated_at, created_by) VALUES ($1, $2, $3, $4, $5, $6) RETURNING id",
        [name, guildId, JSON.stringify(data), ts, ts, createdBy || null]
      );
      return this.get(rows[0].id);
    },
    async update(id, { name, data, createdBy }) {
      const existing = await this.get(id);
      if (!existing) return null;
      const ts = now();
      await pool.query(
        "UPDATE templates SET name = $1, data = $2, updated_at = $3, created_by = COALESCE($4, created_by) WHERE id = $5",
        [name ?? existing.name, data ? JSON.stringify(data) : existing.data, ts, createdBy || null, Number(id)]
      );
      return this.get(id);
    },
    async delete(id) {
      await pool.query("DELETE FROM templates WHERE id = $1", [Number(id)]);
    },
  };
}

// ---------------------------------------------------------------------------
// SQLite backend
// ---------------------------------------------------------------------------
function createSqliteBackend() {
  const db = new DatabaseSync(SQLITE_PATH);
  db.exec(TABLE_SQL);

  return {
    kind: "sqlite",
    list(guildId) {
      const rows = db
        .prepare(
          "SELECT id, name, guild_id, created_at, updated_at, created_by FROM templates WHERE guild_id = ? ORDER BY updated_at DESC"
        )
        .all(guildId);
      return rows.map((r) => ({
        id: r.id,
        name: r.name,
        guildId: r.guild_id,
        createdAt: r.created_at,
        updatedAt: r.updated_at,
        createdBy: r.created_by,
      }));
    },
    get(id) {
      const r = db
        .prepare("SELECT * FROM templates WHERE id = ?")
        .get(Number(id));
      if (!r) return null;
      let data = r.data;
      try {
        data = JSON.parse(r.data);
      } catch {
        /* keep as-is if not JSON */
      }
      return { ...r, guildId: r.guild_id, createdBy: r.created_by, data };
    },
    create({ name, guildId, data, createdBy }) {
      const ts = now();
      const info = db
        .prepare(
          "INSERT INTO templates (name, guild_id, data, created_at, updated_at, created_by) VALUES (?, ?, ?, ?, ?, ?)"
        )
        .run(name, guildId, JSON.stringify(data), ts, ts, createdBy || null);
      return this.get(info.lastInsertRowid);
    },
    update(id, { name, data, createdBy }) {
      const existing = this.get(id);
      if (!existing) return null;
      const ts = now();
      db.prepare(
        "UPDATE templates SET name = ?, data = ?, updated_at = ?, created_by = COALESCE(?, created_by) WHERE id = ?"
      ).run(
        name ?? existing.name,
        data ? JSON.stringify(data) : existing.data,
        ts,
        createdBy || null,
        Number(id)
      );
      return this.get(id);
    },
    delete(id) {
      db.prepare("DELETE FROM templates WHERE id = ?").run(Number(id));
    },
  };
}

// ---------------------------------------------------------------------------
// JSON fallback backend
// ---------------------------------------------------------------------------
function createJsonBackend() {
  const load = () => {
    try {
      return JSON.parse(fs.readFileSync(JSON_PATH, "utf-8"));
    } catch {
      return { seq: 1, items: [] };
    }
  };
  const save = (store) => fs.writeFileSync(JSON_PATH, JSON.stringify(store, null, 2));

  return {
    kind: "json",
    list(guildId) {
      return load()
        .items.filter((t) => t.guildId === guildId)
        .sort((a, b) => b.updatedAt - a.updatedAt)
        .map(({ data, ...meta }) => meta);
    },
    get(id) {
      return load().items.find((t) => String(t.id) === String(id)) || null;
    },
    create({ name, guildId, data, createdBy }) {
      const store = load();
      const ts = now();
      const item = {
        id: store.seq++,
        name,
        guildId,
        data,
        createdAt: ts,
        updatedAt: ts,
        createdBy: createdBy || null,
      };
      store.items.push(item);
      save(store);
      return item;
    },
    update(id, { name, data, createdBy }) {
      const store = load();
      const item = store.items.find((t) => String(t.id) === String(id));
      if (!item) return null;
      if (name !== undefined) item.name = name;
      if (data !== undefined) item.data = data;
      if (createdBy) item.createdBy = createdBy;
      item.updatedAt = now();
      save(store);
      return item;
    },
    delete(id) {
      const store = load();
      store.items = store.items.filter((t) => String(t.id) !== String(id));
      save(store);
    },
  };
}

// ---------------------------------------------------------------------------
// Select backend
// ---------------------------------------------------------------------------
let backend;
if (process.env.DATABASE_URL) {
  try {
    backend = await createPostgresBackend();
    console.log("[dashboard] Using Postgres template store (DATABASE_URL).");
  } catch (e) {
    console.warn("[dashboard] Postgres unavailable, falling back to local store:", e.message);
    try {
      backend = createSqliteBackend();
    } catch {
      backend = createJsonBackend();
    }
  }
} else {
  try {
    backend = createSqliteBackend();
  } catch (e) {
    console.warn("[dashboard] node:sqlite unavailable, falling back to JSON store:", e.message);
    backend = createJsonBackend();
  }
}

export const dbBackend = backend;
export const dbKind = backend.kind;

export async function listTemplates(guildId) {
  return backend.list(guildId);
}
export async function getTemplate(id) {
  return backend.get(id);
}
export async function createTemplate(input) {
  return backend.create(input);
}
export async function updateTemplate(id, input) {
  return backend.update(id, input);
}
export async function deleteTemplate(id) {
  return backend.delete(id);
}
